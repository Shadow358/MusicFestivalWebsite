<div class="p_text">
<p>Matthias Paul, better known by his stage name Paul van Dyk (born 16 December 1971) is a German Grammy Award-winning electronic dance music DJ, musician and record producer. One of the first true superstar DJs, Paul van Dyk was the first artist to receive a Grammy nomination in the newly added category of Best Dance/Electronic album for his 2003 release Reflections. He was named the World's No. 1 DJ in both 2005 and 2006, something less than a handful of DJs have ever achieved. Paul van Dyk is the only DJ to hold a spot in the World's Top 10 DJs since 1998. He is also the first DJ to be named No. 1 by Mixmag in 2005. As of 2007, he sold over 4.5 million albums worldwide.
A trance producer starting in the early 1990s, Paul quickly achieved popularity with his remix of "Love Stimulation" by Humate on the record label MFS in 1993 and with his hit single "For an Angel" but, in recent times, he no longer likes to describe his music as trance, but rather simply as electronic dance music. <br>
